    private void btn_inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inputActionPerformed
        // TODO add your handling code here:
        try {
            transaksi u = new transaksi();
            text_total_bayar.setText("" + u.total_bayar);
            this.stat = k.getCon().prepareStatement("insert into transaksi values(?,?,?,?,?,?,?,?)");
            stat.setInt(1, 0);
            stat.setString(2, u.nama_pelanggan);
            stat.setInt(3, u.id_film);
            stat.setString(4, u.tanggal);
            stat.setString(5, u.nama_film);
            stat.setInt(6, u.harga);
            stat.setInt(7, u.jumlah_beli);
            stat.setInt(8, u.total_bayar);
            int pilihan = JOptionPane.showConfirmDialog(null,
                    "Tanggal: "+u.tanggal+
                    "\nNama Pelanggan: "+u.nama_pelanggan+
                    "\nPembelian: "+u.jumlah_beli+" "+u.nama_film+
                    "\nTotal Bayar: "+u.total_bayar+"\n",
                    "Tambahkan Transaksi?",
                    JOptionPane.YES_NO_OPTION);
            if (pilihan == JOptionPane.YES_OPTION) {
                stat.executeUpdate();
                refreshTable();
            } else if(pilihan == JOptionPane.NO_OPTION) {
                refreshTable();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_inputActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        try {
            transaksi u = new transaksi();
            u.id_transaksi = Integer.parseInt(text_id_transaksi.getText());
            this.stat = k.getCon().prepareStatement("update transaksi set nama_pelanggan=?,"
                    + "id_film=?,tanggal=?,nama_film=?,harga=?,jumlah_beli=?,total_bayar=? "
                    + "where id_transaksi=?");
            
            stat.setString(1, u.nama_pelanggan);
            stat.setInt(2, u.id_film);
            stat.setString(3, u.tanggal);
            stat.setString(4, u.nama_film);
            stat.setInt(5, u.harga);
            stat.setInt(6, u.jumlah_beli);
            stat.setInt(7, u.total_bayar);
            stat.setInt(8, u.id_transaksi);
            stat.executeUpdate();
            refreshTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_btn_updateActionPerformed

