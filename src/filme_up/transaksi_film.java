    private class transaksi {
        int id_transaksi, id_film, harga, jumlah_beli, total_bayar;
        String nama_pelanggan, tanggal, nama_film;

        public transaksi() {
            this.nama_pelanggan = text_nama_pelanggan.getText();
            String combo = combo_id_film.getSelectedItem().toString();
            String[] arr = combo.split(":");
            this.id_film = Integer.parseInt(arr[0]);
            try {
                java.util.Date date = text_tanggal.getDate();
                DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                this.tanggal = dateFormat.format(date);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            this.nama_film = arr[1];
            this.harga = Integer.parseInt(arr[2]);
            this.jumlah_beli = Integer.parseInt(text_total_tiket.getText());
            this.total_bayar = this.harga * this.jumlah_beli;
        }
    }
    
    public void refreshTable(){
        model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("Nama Pelanggan");
        model.addColumn("ID Film");
        model.addColumn("Tanggal");
        model.addColumn("Nama Film");
        model.addColumn("Harga");
        model.addColumn("Jumlah Beli");
        model.addColumn("Total Bayar");
        table_transaksi.setModel(model);
        try {
            this.stat = k.getCon().prepareStatement("select * from transaksi");
            this.rs = this.stat.executeQuery();
            while (rs.next()) { 
                Object[] data = {
                  rs.getString(1),
                  rs.getString(2),
                  rs.getString(3),
                  rs.getString(4),
                  rs.getString(5),
                  rs.getString(6),
                  rs.getString(7),
                  rs.getString(8)
                };
                model.addRow(data);
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        text_id_transaksi.setText("");
        text_nama_pelanggan.setText("");
        text_total_tiket.setText("");
        text_total_bayar.setText("");  
    }
    
    public void refreshCombo(){
        try {
            this.stat = k.getCon().prepareStatement("select * from film "
                    + "where status ='Tersedia'");
            this.rs = this.stat.executeQuery();
            while (rs.next()) {
                combo_id_film.addItem(rs.getString("id_film")+":"
                +rs.getString("nama_film")+":"+rs.getString("harga"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

